## Nonlocal practice
def vending_machine(snacks):
    """Cycles through sequence of snacks.

    >>> vender = vending_machine(("chips", "chocolate", "popcorn"))
    >>> vender()
    "chips"
    >>> vender()
    "chocolate"
    >>> vender()
    "popcorn"
    >>> vender()
    "chips"
    >>> other = vending_machine(("brownie",))
    >>> other()
    "brownie"
    >>> vender()
    "chocolate"
    """
    "*** YOUR CODE HERE ***"
    counter = 0
    def vend():
        nonlocal counter
        counter, snack = counter + 1, snacks[counter % 3]
        return snack
    return vend
